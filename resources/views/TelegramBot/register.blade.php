<b>Шаг 1: Отправка телефона</b>

Важно: используйте для регистрации мобильное приложение Telegram (не веб-версию).

Для регистрации нажмите на кнопку "Отправить телефон" в меню бота, чтобы поделиться им с нами.

Если кнопка не появилась, нажмите на значок рядом с иконкой отправки сообщения, появится меню с кнопкой.